package model;

public class Product {

	private int code;
	private String name;
	private String description;
	private double cost;
	private String nitRes;
	

	
	public Product(int code, String name, String description, double cost, String nitRes) {
		this.code = code;
		this.name = name;
		this.description = description;
		this.cost = cost;
		this.nitRes = nitRes;
	}





	
	
}
